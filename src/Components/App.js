import { useEffect, useState } from "react";
import Main from "./Main";
import NavBar from "./NavBar";
import Box from "./Box";
import Loader from "./Loader";
import MovieDetails from "./MovieDetails";
import WatchedSummary from "./WatchedSummary";
import WatchedMoviesList from "./WatchedMoviesList";
import Input from "./input";
import FoundResults from "./FoundResults";
import ErrorMsg from "./ErrorMsg";
import MovieList from "./MovieList";

const KEY = "9e3f9507";
export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsSetLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  function handleSelectedId(id) {
    setSelectedId((prev) => (prev === id ? null : id));
  }
  function handleBack() {
    setSelectedId(null);
  }
  function addWatchedMovie(movie) {
    setWatched((prev) => [...prev, movie]);
  }
  function handleDeleteWatchedMovie(id) {
    setWatched((prev) => prev.filter((ele) => ele.imdbID !== id));
  }
  useEffect(() => {
    const controller = new AbortController();
    async function fetchMovies() {
      try {
        setIsSetLoading(true);
        setError("");
        const res = await fetch(
          `https://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
          { signal: controller.signal }
        );

        if (!res.ok)
          throw new Error("Something went wrong while fetching movies");

        const data = await res.json();
        if (data.Response === "False") throw new Error("Movie not found");

        setMovies(data.Search);
        setError("");
      } catch (err) {
        console.log(err.message);
        if (err.name !== "AbortError") {
          setError(err.message);
        }
      } finally {
        setIsSetLoading(false);
      }
    }
    if (query.length < 3) {
      setMovies([]);
      setError("");
      return;
    }
    handleBack();
    fetchMovies();
    return function () {
      controller.abort();
    };
  }, [query]);
  return (
    <>
      <NavBar>
        <Input query={query} setQuery={setQuery} />
        <FoundResults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList
              movies={movies}
              onSelectId={handleSelectedId}
              selectedId={selectedId}
            />
          )}
          {error && <ErrorMsg message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onBack={handleBack}
              onAddWatchedMovie={addWatchedMovie}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatchedMovie={handleDeleteWatchedMovie}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

